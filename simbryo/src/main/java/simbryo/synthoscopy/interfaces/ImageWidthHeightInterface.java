package simbryo.synthoscopy.interfaces;

/**
 * Image width and height interface
 *
 * @author royer
 */
public interface ImageWidthHeightInterface
{

  /**
   * Returns the rendered image width
   * 
   * @return image width
   */
  long getWidth();

  /**
   * Returns the rendered image height
   * 
   * @return image height
   */
  long getHeight();

}
