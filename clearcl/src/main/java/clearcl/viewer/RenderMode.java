package clearcl.viewer;

/**
 * Enum listing the different possible render modes
 *
 * @author royer
 */
public enum RenderMode
{
 /**
  * Slice rendering (single plane)
  */
 Slice,

 /**
  * Max projection rendering
  */
 MaxProjection,

 /**
  * Average projection rendering
  */
 AvgProjection,

 /**
  * Colorprojection rendering
  */
 ColorProjection
}
