package clearcontrol;

/**
 * Light sheet microscope DOFs
 *
 * @author royer
 */
public enum LightSheetDOF
{
  /**
   * Detection Z (focus)
   */
  DZ,

  /**
   * Illumination X
   */
  IX,

  /**
   * illumination Y
   */
  IY,

  /**
   * Illumination Z
   */
  IZ,

  /**
   * Illumination alpha
   */
  IA,

  /**
   * Illumination beta
   */
  IB,

  /**
   * Illumination width
   */
  IW,

  /**
   * Iluumination height
   */
  IH,

  /**
   * illumination power/intensity
   */
  IP,

  /**
   * illumination arm index
   */
  II
}
